// Genera el emoji bandera desde el código ISO-2
function codeToFlag(code) {
  if (!code || code.length !== 2) return "🌐";
  const A = 0x1f1e6;
  return String.fromCodePoint(
    ...code
      .toUpperCase()
      .split("")
      .map((c) => A + c.charCodeAt(0) - 65),
  );
}

ISO_CODES.forEach((code) => {
  FLAGS[code] = codeToFlag(code);
});

// Función pública: obtener bandera de cualquier código
export function getFlag(code) {
  return FLAGS[code] || codeToFlag(code) || "🌐";
}
